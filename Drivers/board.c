volatile static uint8_t irq_level;

void hw_interrupt_enable(void)
{
	irq_level--;
	if(irq_level == 0)
	{
	  __enable_irq(); 
	}
}

void hw_interrupt_disable(void)
{
	__disable_irq(); 
	irq_level++;
}

void interrupt_enter(void)
{
	__disable_irq(); 
}

void interrupt_leave(void)
{
	__enable_irq(); 
}



